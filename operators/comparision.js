let a = 10;
let b = "12";
let c = 15;

console.log("a =", a, ", b =", b, ", c =", c);

// Equal to (==)
console.log("a == b :", a == b); 

// Not equal (!=)
console.log("a != b :", a != b); 

// Strictly equal (===)
console.log("a === b :", a === b);

// Strictly not equal (!==)
console.log("a !== b :", a !== b); 

// Greater than (>)
console.log("c > a :", c > a); 

// Less than (<)
console.log("a < c :", a < c);

// Greater than or equal (>=)
console.log("a >= b :", a >= b);